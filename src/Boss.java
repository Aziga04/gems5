public class Boss {
    int health;
    int damage;
    int defence;

    public int getHealth() {
        return health;
    }
    public void setHealth(int i){
        this.health = health;
    }

    public int getDamage(){
        return damage;
    }

    public void setDamage(int i){
        this.damage = damage;
    }

    public int getDefence(){
        return defence;
    }
    public void setDefence(int i){
        this.defence = defence;

    }
}
